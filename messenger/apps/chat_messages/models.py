class GroupMessageEncryption(models.Model):
    """Group-message sender-key encryption metadata.

    The ciphertext itself stays in Message.encrypted_payload.
    This model stores only public routing/verification metadata needed for
    group sender-key decryption. It must never store plaintext, message keys,
    sender-chain secrets, private signing keys, recovery secrets, or ratchet
    state.
    """

    id = models.UUIDField(
        primary_key=True,
        default=uuid.uuid4,
        editable=False,
    )

    message = models.OneToOneField(
        Message,
        on_delete=models.CASCADE,
        related_name="group_encryption",
    )

    group_room = models.ForeignKey(
        "rooms.Room",
        on_delete=models.CASCADE,
        related_name="group_message_encryptions",
    )

    epoch = models.ForeignKey(
        "group_chat.GroupEncryptionEpoch",
        on_delete=models.PROTECT,
        related_name="group_message_encryptions",
    )

    sender_key = models.ForeignKey(
        "group_chat.GroupSenderKey",
        on_delete=models.PROTECT,
        related_name="group_message_encryptions",
    )

    chain_iteration = models.PositiveIntegerField()

    signature = models.TextField()

    encryption_metadata = models.JSONField(
        default=dict,
    )

    created_at = models.DateTimeField(
        auto_now_add=True,
    )

    class Meta:
        db_table = "messenger_group_message_encryptions"
        ordering = [
            "-created_at",
            "-id",
        ]
        constraints = [
            models.UniqueConstraint(
                fields=[
                    "sender_key",
                    "chain_iteration",
                ],
                name="uniq_group_sender_iter",
            ),
            models.CheckConstraint(
                check=models.Q(chain_iteration__gte=0),
                name="group_msg_iter_gte_0",
            ),
        ]
        indexes = [
            models.Index(
                fields=[
                    "group_room",
                    "epoch",
                    "-created_at",
                ],
                name="gmenc_room_epoch_idx",
            ),
            models.Index(
                fields=[
                    "sender_key",
                    "chain_iteration",
                ],
                name="gmenc_sender_iter_idx",
            ),
        ]

    def __str__(self) -> str:
        return (
            f"Group message {self.message_id} "
            f"epoch={self.epoch_id} iteration={self.chain_iteration}"
        )

    def clean(self) -> None:
        super().clean()

        self.signature = self.signature.strip()

        errors = {}

        if not self.signature:
            errors["signature"] = "Group message signature is required."

        if not isinstance(self.encryption_metadata, dict):
            errors["encryption_metadata"] = (
                "Encryption metadata must be a JSON object."
            )

        if self.message_id and self.group_room_id:
            if self.message.room_id != self.group_room_id:
                errors["message"] = (
                    "Message room must match group encryption room."
                )

        if self.group_room_id:
            if self.group_room.room_type != "group":
                errors["group_room"] = (
                    "Group message encryption can only attach to group rooms."
                )

        if self.epoch_id and self.group_room_id:
            if self.epoch.group_room_id != self.group_room_id:
                errors["epoch"] = "Epoch does not belong to this group."

        if self.sender_key_id and self.group_room_id:
            if self.sender_key.group_room_id != self.group_room_id:
                errors["sender_key"] = (
                    "Sender key does not belong to this group."
                )

        if self.sender_key_id and self.epoch_id:
            if self.sender_key.epoch_id != self.epoch_id:
                errors["sender_key"] = (
                    "Sender key does not belong to this epoch."
                )

        if self.message_id and self.sender_key_id:
            if self.message.sender_user_id != self.sender_key.sender_user_id:
                errors["message"] = (
                    "Message sender must match sender-key owner."
                )

            if (
                str(self.message.sender_device_id)
                != str(self.sender_key.sender_device_id)
            ):
                errors["message"] = (
                    "Message sender device must match sender-key device."
                )

        forbidden_words = (
            "plaintext",
            "private_key",
            "secret",
            "sender_chain_secret",
            "message_key",
            "ratchet",
            "recovery_key",
        )

        for key in self.encryption_metadata.keys():
            normalized_key = str(key).lower()
            if any(word in normalized_key for word in forbidden_words):
                errors["encryption_metadata"] = (
                    "Encryption metadata must not contain plaintext or "
                    "secret cryptographic material."
                )
                break

        if errors:
            raise ValidationError(errors)



class ContactDeliveryPolicy(models.Model):
    """
    Messenger-local read projection of identity contact delivery policy.

    Identity is the source of truth. Messenger only reads this table during
    message send, so direct-message delivery never calls identity.
    """

    id = models.UUIDField(
        primary_key=True,
        default=uuid.uuid4,
        editable=False,
    )

    owner_user_id = models.CharField(
        max_length=EXTERNAL_USER_ID_MAX_LENGTH,
    )

    target_user_id = models.CharField(
        max_length=EXTERNAL_USER_ID_MAX_LENGTH,
    )

    is_blocked = models.BooleanField(
        default=False,
    )
    ghost_until = models.DateTimeField(
        null=True,
        blank=True,
    )

    ghost_permanent = models.BooleanField(
        default=False,
    )

    ghost_duration_option = models.CharField(
        max_length=20,
        blank=True,
        default="",
    )

    policy_version = models.PositiveIntegerField(
        default=1,
    )

    source_updated_at = models.DateTimeField(
        null=True,
        blank=True,
    )

    synced_at = models.DateTimeField(
        auto_now=True,
    )

    created_at = models.DateTimeField(
        auto_now_add=True,
    )

    class Meta:
        db_table = "messenger_contact_delivery_policies"
        constraints = [
            models.UniqueConstraint(
                fields=[
                    "owner_user_id",
                    "target_user_id",
                ],
                name="uniq_contact_policy_owner_target",
            ),
            models.CheckConstraint(
                condition=~Q(owner_user_id=models.F("target_user_id")),
                name="ck_contact_policy_not_self",
            ),
        ]
        indexes = [
            models.Index(
                fields=[
                    "owner_user_id",
                    "target_user_id",
                    "is_blocked",
                ],
                name="contact_policy_lookup_idx",
            ),
        ]

    def clean(self) -> None:
        super().clean()

        self.owner_user_id = str(self.owner_user_id).strip()
        self.target_user_id = str(self.target_user_id).strip()
        
        errors = {}
        allowed_ghost_options = {
            "",
            "1h",
            "6h",
            "12h",
            "24h",
            "permanent",
        }

        self.ghost_duration_option = str(
            self.ghost_duration_option or ""
        ).strip()



        if self.ghost_duration_option not in allowed_ghost_options:
            errors["ghost_duration_option"] = (
                "Ghost duration option is invalid."
            )
        if not self.owner_user_id:
            errors["owner_user_id"] = "Owner user ID is required."

        if not self.target_user_id:
            errors["target_user_id"] = "Target user ID is required."

        if (
            self.owner_user_id
            and self.target_user_id
            and self.owner_user_id == self.target_user_id
        ):
            errors["target_user_id"] = (
                "Target user ID must be different from owner user ID."
            )

        if self.policy_version < 1:
            errors["policy_version"] = (
                "Policy version must be at least 1."
            )

        if errors:
            raise ValidationError(errors)



class DirectMessageReceiptDecision(models.Model):
    """
    Per-message receipt policy decision.

    This is created at message-send time. If a user was ghosting the sender
    at that moment, delivered/read receipts stay suppressed forever for that
    message, even after the ghost duration expires.
    """

    class PolicyReason(models.TextChoices):
        NORMAL = "normal", "Normal"
        BLOCKED = "blocked", "Blocked"
        GHOST = "ghost", "Ghost"

    id = models.UUIDField(
        primary_key=True,
        default=uuid.uuid4,
        editable=False,
    )

    message = models.OneToOneField(
        Message,
        on_delete=models.CASCADE,
        related_name="receipt_decision",
    )

    sender_user_id = models.CharField(
        max_length=EXTERNAL_USER_ID_MAX_LENGTH,
    )

    recipient_user_id = models.CharField(
        max_length=EXTERNAL_USER_ID_MAX_LENGTH,
    )

    suppress_delivered_receipt = models.BooleanField(
        default=False,
    )

    suppress_read_receipt = models.BooleanField(
        default=False,
    )

    policy_reason = models.CharField(
        max_length=20,
        choices=PolicyReason.choices,
        default=PolicyReason.NORMAL,
    )

    policy_version = models.PositiveIntegerField(
        null=True,
        blank=True,
    )

    created_at = models.DateTimeField(
        auto_now_add=True,
    )

    class Meta:
        db_table = "messenger_direct_message_receipt_decisions"
        indexes = [
            models.Index(
                fields=[
                    "message",
                    "recipient_user_id",
                ],
                name="dm_rec_dec_msg_user_idx",
            ),
            models.Index(
                fields=[
                    "recipient_user_id",
                    "sender_user_id",
                    "policy_reason",
                ],
                name="dm_receipt_decision_pair_idx",
            ),
        ]

    def clean(self) -> None:
        super().clean()

        self.sender_user_id = str(self.sender_user_id).strip()
        self.recipient_user_id = str(self.recipient_user_id).strip()

        errors = {}

        if not self.sender_user_id:
            errors["sender_user_id"] = "Sender user ID is required."

        if not self.recipient_user_id:
            errors["recipient_user_id"] = "Recipient user ID is required."

        if (
            self.sender_user_id
            and self.recipient_user_id
            and self.sender_user_id == self.recipient_user_id
        ):
            errors["recipient_user_id"] = (
                "Recipient user ID must be different from sender user ID."
            )

        if self.policy_version is not None and self.policy_version < 1:
            errors["policy_version"] = (
                "Policy version must be at least 1."
            )

        if errors:
            raise ValidationError(errors)